package com.InstituteManagementSystem.Institute.Management.System.Model;
/**
 The Teacher class represents a teacher in the Institute Management System.
 */
public class Teacher {
    /**
     * The ID of the teacher.
     */
    public int id;

    /**
     * The name of the teacher.
     */
    public String name;

    /**
     * The email address of the teacher.
     */
    public String email;

    /**
     * The salary of the teacher.
     */
    public Double salary;

}
